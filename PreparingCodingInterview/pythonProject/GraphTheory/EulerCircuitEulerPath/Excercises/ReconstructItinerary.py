from typing import List, DefaultDict as SparseGraph, Set
from collections import defaultdict

class FindItinerarySolution:
    def __init__(self, tickets: List[List[str]]):

        self.tickets: List[List[str]] = tickets

    def solve(self) -> List[str]:

        g: SparseGraph[str, List[str]] = defaultdict(list)

        for f, t in self.tickets:
            g[f].append(t)


        for k in g.keys():
            g[k].sort()

        def euler_walk(u: str) -> List[str]:

            path: List[str] = [u]

            cur_u = u

            while g[cur_u]:
                cur_v = g[cur_u].pop(0)
                path.append(cur_v)
                cur_u = cur_v

            p = len(path)-1
            entries = list(path)
            for p in range(len(entries), 0, -1):
                v = entries[p-1]
                if not g[v]:
                    continue
                sub_circuit = euler_walk(v)[1:]
                path = path[:p] + sub_circuit + path[p:]

            return path

        result = euler_walk('JFK')


        return result




